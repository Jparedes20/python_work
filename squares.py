#Using range() to make a list() of numbers

print("List of numbers:\n"+str(list(range(1,50))))#This code will print a list of 10 numbers
print("\neven numbers:\n"+str(list(range(2,50,2))))#This code will print all even numbers in the list
print("\nOdd numbers in the list:\n"+str(list(range(1,50,2))))#This code will print all odd numbers in the list

